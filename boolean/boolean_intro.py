# Example 1: Boolean values
x = True
y = False
print("x:", x, "y:", y)

# Example 2: Boolean as comparison result
a = 5
b = 10
print("a == b:", a == b)
print("a < b:", a < b)

# Example 3: Boolean operators - and
print((a < b) and (b > 0))

# Example 4: Boolean operators - or
print((a > b) or (b > 0))

# Example 5: Boolean operator not
print(not(a > b))
 
