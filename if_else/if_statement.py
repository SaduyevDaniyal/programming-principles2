# Example 1: simple if
x = 10
if x > 5:
    print("x is greater than 5")

# Example 2: if with number
y = 3
if y < 5:
    print("y is less than 5")

# Example 3: if with string
name = "Alice"
if name == "Alice":
    print("Hello Alice!")

# Example 4: if with boolean
flag = True
if flag:
    print("Flag is set")

# Example 5: if with calculation
a = 7
if a % 2 == 1:
    print("a is odd")
